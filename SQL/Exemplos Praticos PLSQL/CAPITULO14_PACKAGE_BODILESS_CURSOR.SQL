CREATE OR REPLACE PACKAGE PKG_CURSOR_EMPLOYEES IS
  CURSOR C_EMPLOYEE_ID (P_EMPLOYEE_ID  EMPLOYEES.EMPLOYEE_ID%TYPE) IS
    SELECT * FROM EMPLOYEES WHERE EMPLOYEE_ID = P_EMPLOYEE_ID;

  CURSOR C_EMPLOYEE_ALL IS
    SELECT * FROM EMPLOYEES;

  CURSOR C_EMPLOYEE_NAME (P_FIRST_NAME  EMPLOYEES.FIRST_NAME%TYPE) IS
    SELECT * FROM EMPLOYEES WHERE FIRST_NAME = P_FIRST_NAME;
END;

CREATE OR REPLACE FUNCTION FUN_CONSULTA_EMP 
(P_EMPLOYEE_ID  EMPLOYEES.EMPLOYEE_ID%TYPE) 
RETURN EMPLOYEES.FIRST_NAME%TYPE IS
BEGIN
FOR V_EMPLOYEE IN PKG_CURSOR_EMPLOYEES.C_EMPLOYEE_ID(P_EMPLOYEE_ID)
   LOOP
   RETURN V_EMPLOYEE.FIRST_NAME;
END LOOP;
RETURN NULL;
END;





 
