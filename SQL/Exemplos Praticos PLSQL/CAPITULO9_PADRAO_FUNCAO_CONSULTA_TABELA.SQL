CREATE OR REPLACE FUNCTION FUN_NOME_TABELA
(P_NOME_PK   NOME_TABELA.NOME_PK%TYPE) 
RETURN NOME_TABELA.NOME_COLUNA%TYPE IS
CURSOR CUR_NOME_TABELA IS
SELECT * FROM NOME_TABELA 
WHERE NOME_PK = P_NOME_PK;
BEGIN
  FOR V_ROW IN CUR_NOME_TABELA LOOP
     RETURN V_ROW.NOME_COLUNA;
  END LOOP;
  RETURN NULL;
END;

