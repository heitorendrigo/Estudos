CREATE OR REPLACE FUNCTION FUN_BUS_NOME_TABELA
(P_NOME_TABELA   NOME_TABELA%ROWTYPE) 
RETURN BOOLEAN IS
BEGIN
  --CHAVE PRIMARIA DEVE SER MAIOR QUE 0 E DIFERENTE DE  NULO
  IF P_NOME_TABELA.NOME_PK IS NULL OR
     P_NOME_TABELA.NOME_PK <= 0 THEN
    RETURN FALSE;
  ELSIF P_NOME_TABELA.NOME_COLUNA1  OPERADOR VALOR THEN
    RETURN FALSE;
  ELSIF P_NOME_TABELA.NOME_COLUNA2  OPERADOR VALOR THEN
    RETURN FALSE;
  ELSIF P_NOME_TABELA.NOME_COLUNA3  OPERADOR VALOR THEN
    RETURN FALSE;
  END IF;
  RETURN TRUE;
END;

--TESTE
SET SERVEROUTPUT ON;
DECLARE
  V_NOME_TABELA NOME_TABELA%ROWTYPE;
  V_RESULTADO   BOOLEAN;
BEGIN
  V_NOME_TABELA.NOME_PK      := '&VALOR_PK';
  V_NOME_TABELA.NOME_COLUNA1 := '&NOME_COLUNA1';
  V_NOME_TABELA.NOME_COLUNA2 := '&NOME_COLUNA2';
  V_NOME_TABELA.NOME_COLUNA3 := '&NOME_COLUNA3';
  V_RESULTADO := FUN_BUS_NOME_TABELA(V_NOME_TABELA);
  IF V_RESULTADO THEN
     DBMS_OUTPUT.PUT_LINE('VALIDAÇÃO OK');
  ELSE
     DBMS_OUTPUT.PUT_LINE('VALIDAÇÃO COM ERROS');
  END IF;
END;





