--CONSULTA TODOS OBJETOS DE PROGRAMAÇÃO DE UM SCHEMA
SELECT OBJECT_NAME, OBJECT_TYPE,STATUS
FROM ALL_OBJECTS
WHERE OWNER = USER AND OBJECT_TYPE IN
('PROCEDURE','FUNCTION','PACKAGE', 
 'PACKAGE BODY','TRIGGER');

--CONSULTA O CODIGO FONTE DE UMA  PROCEDURE OU FUNÇÃO
SELECT TEXT
FROM ALL_SOURCE
WHERE OWNER=USER AND NAME = UPPER('&NOME_ROTINA_PLSQL');

--CONSULTA OS ERROS DE UMA PROCEDURE OU FUNÇÃO

SELECT * 
  FROM ALL_ERRORS
 WHERE ONWER=USER AND NAME = UPPER('&NOME_ROTINA_PLSQL');
 
