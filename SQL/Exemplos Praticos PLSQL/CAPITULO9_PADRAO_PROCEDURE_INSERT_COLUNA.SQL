CREATE OR REPLACE PROCEDURE PRC_INS_NOME_TABELA
(P_NOME_PK       NOME_TABELA.NOME_PK%TYPE,
 P_NOME_COLUNA1  NOME_TABELA.NOME_COLUNA1%TYPE,
 P_NOME_COLUNA2  NOME_TABELA.NOME_COLUNA2%TYPE,
 P_NOME_COLUNA3  NOME_TABELA.NOME_COLUNA3%TYPE,
 P_NOME_COLUNA4  NOME_TABELA.NOME_COLUNA4%TYPE,
 P_NOME_COLUNA5  NOME_TABELA.NOME_COLUNA5%TYPE,
 P_NOME_COLUNA6  NOME_TABELA.NOME_COLUNA6%TYPE,
 P_NOME_COLUNA7  NOME_TABELA.NOME_COLUNA7%TYPE,
 P_NOME_COLUNA8  NOME_TABELA.NOME_COLUNA8%TYPE,
 P_NOME_COLUNA9  NOME_TABELA.NOME_COLUNA9%TYPE,
 P_COMMIT      BOOLEAN DEFAULT FALSE) IS
E_ERRO_FK       EXCEPTION;
PRAGMA EXCEPTION_INIT(E_ERRO_FK ,-2292);
BEGIN
    INSERT INTO NOME_TABELA (NOME_PK,NOME_COLUNA1,NOME_COLUNA2,NOME_COLUNA3,NOME_COLUNA4,
                             NOME_COLUNA5,NOME_COLUNA5,NOME_COLUNA6,NOME_COLUNA7,NOME_COLUNA8,NOME_COLUNA9)
    VALUES (P_NOME_PK,P_NOME_COLUNA1,P_NOME_COLUNA2,P_NOME_COLUNA3,P_NOME_COLUNA4,
            P_NOME_COLUNA5,P_NOME_COLUNA6,P_NOME_COLUNA7,P_NOME_COLUNA8,P_NOME_COLUNA9);

    IF P_COMMIT THEN
       COMMIT;
    END IF;
EXCEPTION
    WHEN  DUP_VAL_ON_INDEX THEN
      DBMS_OUTPUT.PUT_LINE('Erro de codigo de NOME_TABELA já cadastrado');
    WHEN  E_ERRO_FK THEN
      DBMS_OUTPUT.PUT_LINE('Erro de valor em coluna relacionada');
    WHEN OTHERS THEN
      DBMS_OUTPUT.PUT_LINE('Erro de INSERT na tabela NOME_TABELA');
END;  
