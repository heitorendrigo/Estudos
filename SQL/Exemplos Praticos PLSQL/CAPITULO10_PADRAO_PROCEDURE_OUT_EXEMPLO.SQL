CREATE OR REPLACE PROCEDURE PRC_SEL_DEPARTMENTS
(P_DEPARTMENT_ID      DEPARTMENTS.DEPARTMENT_ID%TYPE,
 P_DEPARTMENT_NAME OUT DEPARTMENTS.DEPARTMENT_NAME%TYPE,
 P_MANAGER_ID OUT DEPARTMENTS.MANAGER_ID%TYPE) IS
 CURSOR CUR_DEPARTMENTS IS
   SELECT DEPARTMENT_NAME,MANAGER_ID
     FROM DEPARTMENTS
    WHERE DEPARTMENT_ID = P_DEPARTMENT_ID;
 BEGIN
     P_DEPARTMENT_NAME := NULL;
     P_MANAGER_ID := NULL;
   FOR V_DEPARTMENTS IN CUR_DEPARTMENTS LOOP
     P_DEPARTMENT_NAME := V_DEPARTMENTS.DEPARTMENT_NAME;
     P_MANAGER_ID := V_DEPARTMENTS.MANAGER_ID;
   END LOOP;
END; 

--PARA CHAMAR A PROCEDURE
SET SERVEROUTPUT ON;
DECLARE
  V_DEPARTMENT_ID          DEPARTMENTS.DEPARTMENT_ID%TYPE :='&NOME_PK';
  V_DEPARTMENT_NAME   DEPARTMENTS.DEPARTMENT_NAME%TYPE;
  V_MANAGER_ID        DEPARTMENTS.MANAGER_ID%TYPE;
BEGIN
  PRC_SEL_DEPARTMENTS(V_DEPARTMENT_ID,V_DEPARTMENT_NAME,
                      V_MANAGER_ID);
  DBMS_OUTPUT.PUT_LINE('O VALOR_PK ' || V_DEPARTMENT_ID||
                       ' DEPARTMENT_NAME: ' || V_DEPARTMENT_NAME ||
                       ' MANAGER_ID: ' || V_MANAGER_ID);
END;
