CREATE OR REPLACE PROCEDURE PRC_SEL_NOME_TABELA
(P_NOME_PK      NOME_TABELA.NOME_PK%TYPE,
 P_NOME_COLUNA1 OUT NOME_TABELA.NOME_COLUNA1%TYPE,
 P_NOME_COLUNA2 OUT NOME_TABELA.NOME_COLUNA2%TYPE) IS
 CURSOR CUR_NOME_TABELA IS
   SELECT NOME_COLUNA1,NOME_COLUNA2
     FROM NOME_TABELA
    WHERE NOME_PK = P_NOME_PK;
 BEGIN
     P_NOME_COLUNA1 := NULL;
     P_NOME_COLUNA2 := NULL;
   FOR V_NOME_TABELA IN CUR_NOME_TABELA LOOP
     P_NOME_COLUNA1 := V_NOME_TABELA.NOME_COLUNA1;
     P_NOME_COLUNA2 := V_NOME_TABELA.NOME_COLUNA2;
   END LOOP;
END;  

--CHAMADA A PROCEDURE
SET SERVEROUTPUT ON;
DECLARE
  V_NOME_PK        NOME_TABELA.NOME_PK%TYPE :='&NOME_PK';
  V_NOME_COLUNA1   NOME_TABELA.NOME_COLUNA1%TYPE;
  V_NOME_COLUNA2   NOME_TABELA.NOME_COLUNA2%TYPE;
BEGIN
  PRC_SEL_NOME_TABELA(V_NOME_PK,V_NOME_COLUNA1,
                      V_NOME_COLUNA2);
  DBMS_OUTPUT.PUT_LINE('O VALOR_PK ' || V_NOME_PK ||
                       ' NOME_COLUNA1: ' || V_NOME_COLUNA1 ||
                       ' NOME_COLUNA2: ' || V_NOME_COLUNA2);
END;