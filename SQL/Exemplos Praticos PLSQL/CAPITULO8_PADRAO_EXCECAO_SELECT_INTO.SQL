SET SERVEROUTPUT ON;
DECLARE
V_NOME_TABELA   NOME_TABELA%ROWTYPE;
BEGIN
    SELECT * 
      INTO V_NOME_TABELA
      FROM NOME_TABELA
      --WHERE 
      ;
EXCEPTION
    WHEN  NO_DATA_FOUND THEN
      DBMS_OUTPUT.PUT_LINE('NOME_TABELA não encontrado');
    WHEN TOO_MANY_ROWS THEN
      DBMS_OUTPUT.PUT_LINE('NOME_TABELA retornou mais registros que o esperado');
END;  
