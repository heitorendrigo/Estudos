SET SERVEROUTPUT ON;
DECLARE
   V_NOME_TABELA          NOME_TABELA%ROWTYPE;
   V_NOME_COLUNA_PK       NOME_TABELA.NOME_COLUNA_PK%TYPE :='VALOR_INI';
BEGIN
   SELECT *
     INTO V_NOME_TABELA
     FROM NOME_TABELA
    WHERE NOME_COLUNA_PK = V_NOME_COLUNA_PK;
   DBMS_OUTPUT.PUT_LINE('O VALOR DA COLUNA1 EH:' ||
                        V_NOME_TABELA.NOME_COLUNA1);
END;

