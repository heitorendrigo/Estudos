SET SERVEROUTPUT ON;
DECLARE
  CURSOR CUR_EMPLOYEES (P_FIRST_NAME  EMPLOYEES.FIRST_NAME%TYPE) IS
  SELECT * FROM EMPLOYEES WHERE FIRST_NAME LIKE INITCAP(P_FIRST_NAME) || '%';
BEGIN
  FOR V_REGISTRO IN CUR_EMPLOYEES('A') LOOP
      DBMS_OUTPUT.PUT_LINE(V_REGISTRO.EMPLOYEE_ID || ' ' ||
                           V_REGISTRO.FIRST_NAME);
  END LOOP;
END;  