CREATE OR REPLACE PROCEDURE PRC_INS_BUS_NOME_TABELA
(P_NOME_TABELA   NOME_TABELA%ROWTYPE,
 P_COMMIT        BOOLEAN,
 P_RESULTADO     IN OUT VARCHAR2) IS
 V_RESULTADO  BOOLEAN  := FUN_BUS_NOME_TABELA
 (P_NOME_TABELA   NOME_TABELA%ROWTYPE);
BEGIN
 IF NOT v_RESULTADO THEN
   P_RESULTADO :='ERRO DE VALIDACAO';
   RETURN;
 ELSE 
   P_RESULTADO := NULL;
 END IF;
 PRC_INS_ROW_NOME_TABELA(P_NOME_TABELA,P_COMMIT);
END;

